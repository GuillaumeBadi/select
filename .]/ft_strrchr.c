#include "libft.h"

char			*ft_strrchr(char const *s, int c)
{
	int			i;
	char		d;

	i = ft_strlen(s);
	d = c;
	while (i >= 0 && s[i] != d)
	{
		i--;
		if (!s[i])
			return (NULL);
	}
	return ((char*)s + i);
}
