#include "libft.h"
#include <unistd.h>

char			ft_getc(int fd)
{
	char		b[1];
	int			ret;

	ret = read(fd, b, 1);
	if (ret == 0)
		return (0);
	if (ret < 0)
		return (-1);
	else
		return (*b);
}
